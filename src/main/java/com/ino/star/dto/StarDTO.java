package com.ino.star.dto;

public class StarDTO {

	private int star_no;
	private String star_to_id;
	private String star_div;
	private int star_div_no;
	
	public int getStar_no() {
		return star_no;
	}
	public void setStar_no(int star_no) {
		this.star_no = star_no;
	}
	public String getStar_to_id() {
		return star_to_id;
	}
	public void setStar_to_id(String star_to_id) {
		this.star_to_id = star_to_id;
	}
	public String getStar_div() {
		return star_div;
	}
	public void setStar_div(String star_div) {
		this.star_div = star_div;
	}
	public int getStar_div_no() {
		return star_div_no;
	}
	public void setStar_div_no(int star_div_no) {
		this.star_div_no = star_div_no;
	}
	
}
